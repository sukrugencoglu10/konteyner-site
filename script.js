                    const operation = document.getElementById('operation').value;
                    const containerType = document.getElementById('container-type').value;

                    // 2. İsim Parçala (Meta Eşleşmesi İçin)
                    const nameParts = nameInput.split(' ');
                    const firstName = nameParts[0];
                    const lastName = nameParts.slice(1).join(' ') || '';

                    // 3. Benzersiz Event ID
                    const eventID = 'lead_' + new Date().getTime() + '_' + Math.floor(Math.random() * 1000);

                    // 4. GTM'e Fırlat 🚀
                    window.dataLayer = window.dataLayer || [];
                    window.dataLayer.push({
                        'event': 'GenerateLead',
                        'eventID': eventID,
                        'userData': {
                            'name_full': nameInput,
                            'name_first': firstName,
                            'name_last': lastName,
                            'email': emailInput,
                            'phone': phoneInput
                        },
                        'leadDetails': {
                            'operationType': operation,
                            'containerType': containerType
                        }
                    });

                    console.log("✅ DataLayer Tetiklendi!", { email: emailInput, eventID: eventID });
                    
                    // ==========================================
                    // 🚀 NETLIFY FORMA GÖNDERİM KODU (YENİ EKLENDİ)
                    // ==========================================
